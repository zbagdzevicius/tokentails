export function loadLevelConfig(k: ReturnType<typeof kaboom>, doorState: any) {
    const levelConf = {
        tileWidth: 64,
        tileHeight: 64,
        tiles: {
            "□": () => [
                k.sprite("grass"),
                k.area(),
                k.body({ isStatic: true }),
                k.anchor("bot"),
                k.offscreen({ hide: true }),
                "platform",
            ],
            "-": () => [
                k.sprite("steel"),
                k.area(),
                k.body({ isStatic: true }),
                k.offscreen({ hide: true }),
                k.anchor("bot"),
                openDoors(doorState),
            ],
            T: () => [
                k.sprite("trampoline"),
                k.area(),
                k.body({ isStatic: true }),
                k.anchor("bot"),
                k.offscreen({ hide: true }),
                "trampoline",
            ],
            K: () => [k.sprite("key"), k.area(), k.body(), k.anchor("bot"), k.offscreen({ hide: true }), "key"],
            $: () => [k.sprite("coin"), k.area(), k.pos(0, -9), k.anchor("bot"), k.offscreen({ hide: true }), "coin"],
        },
    };

    return levelConf;
}

function openDoors(doorState: any) {
    return {
        id: "doors",
        require: ["pos", "area"],
        update() {
            if (doorState.isOpen) {
                (this as any).move(0, -30);

                if ((this as any).pos.y <= 3840) {
                    (this as any).pos.y = 3840;
                    doorState.isOpen = false;
                }
            }
        },
    };
}
